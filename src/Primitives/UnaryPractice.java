package Primitives;

public class UnaryPractice {
    public static void main(String[] args) {

        // DD buy 12 get 1$ off next time
        // KK 12 and pay 11$ now
        int DD = 12;
        System.out.println(DD--);
        System.out.println(DD);
        int KK = 12;
        System.out.println(--KK);
        System.out.println(++KK);

    }
}
